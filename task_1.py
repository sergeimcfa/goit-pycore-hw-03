from datetime import datetime


def get_days_from_today(date):
    """
    Функція обчислює кількість днів між заданою датою і поточною датою.

    Аргументи:
        date (str): Дата у форматі 'YYYY-MM-DD'

    Повертає:
        int: кількість днів (додатне або від’ємне число)
        або рядок з повідомленням про помилку, якщо формат введено неправильно
    """
    try:
        # Перетворюємо рядок дати у об’єкт datetime.date
        target_date = datetime.strptime(date, "%Y-%m-%d").date()

        # Отримуємо поточну дату
        today = datetime.today().date()

        # Розраховуємо різницю у днях
        delta = today - target_date

        # Повертаємо різницю у днях
        return delta.days

    except ValueError:
        # Якщо формат дати невірний
        return "Помилка: неправильний формат дати. Використовуйте 'YYYY-MM-DD'."
